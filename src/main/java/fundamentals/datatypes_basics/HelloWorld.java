package fundamentals.datatypes_basics; //pakage name

public class HelloWorld {
    /**
     * public - can be executed from anywhere
     * static - the main object is executed without an object
     * void - main method does not return anything
     * main() - name configured
     * String[] - single argument, array of element of type String
     */
    public static void main(String[] args) {
        System.out.println("Hello world"); // sout + tab IntelliJ shortcut
        // prints Hello World after running the test
    }
}
